package com.github.mrmks.status;

/**
 * This object contains options to adjust some behaviors of StatueValue Interface.
 */
public class ConfigMap {
    private int guiInterval;

    public void setGuiInterval(int guiInterval) {
        this.guiInterval = guiInterval;
    }

    public int getGuiInterval() {
        return guiInterval;
    }
}
